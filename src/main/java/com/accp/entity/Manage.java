package com.accp.entity;

public class Manage {
    private int id ;/*编号*/
    private String name  ;/*名称*/

    public Manage() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
